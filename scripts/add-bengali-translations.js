async function addBengaliTranslations() {
  try {
    console.log('Adding Bengali translations to categories...');
    
    const categories = await prisma.category.findMany();
    let updatedCount = 0;
    
    for (const category of categories) {
      const translation = categoryTranslations[category.name];
      
      if (translation) {
        await prisma.category.update({
          where: { id: category.id },
          data: {
            nameEn: category.name,
            nameBn: translation.nameBn,
            descriptionEn: category.description,
            descriptionBn: translation.descriptionBn
          }
        });
        
        console.log(`âœ“ Added Bengali translation for: ${category.name} â†’ ${translation.nameBn}`);
        updatedCount++;
      } else {
        // Set English as default for both fields if no translation found
        await prisma.category.update({
          where: { id: category.id },
          data: {
            nameEn: category.name,
            nameBn: category.name,
            descriptionEn: category.description,
            descriptionBn: category.description
          }
        });
        console.log(`âš  No translation found for: ${category.name} (using English as default)`);
      }
    }
    
    console.log(`\nðŸŽ‰ Successfully updated ${updatedCount} categories with Bengali translations!`);
    
    // Show summary
    const finalCategories = await prisma.category.findMany({
      select: {
        name: true,
        nameEn: true,
        nameBn: true,
        websites: { select: { id: true } }
      },
      orderBy: { name: 'asc' }
    });
    
    console.log(`\nðŸ“‹ Categories with translations:`);
    finalCategories.forEach((cat, index) => {
      console.log(`${index + 1}. EN: "${cat.nameEn}" | BN: "${cat.nameBn}" (${cat.websites.length} websites)`);
    });
    
  } catch (error) {
    console.error('Error adding Bengali translations:', error);
  } finally {
    await prisma.$disconnect();
  }
}

addBengaliTranslations();
